// Generates src/lib/indonesiaGeo.js from the real Indonesia province
// boundaries published at github.com/ans-4175/peta-indonesia-geojson.
// Run: node scripts/gen-indonesia-map.js
const fs = require("fs");
const path = require("path");
const https = require("https");

const SOURCE_URL =
  "https://raw.githubusercontent.com/ans-4175/peta-indonesia-geojson/master/indonesia-prov.geojson";
const OUT_FILE = path.join(__dirname, "../src/lib/indonesiaGeo.js");

const BOUNDS = { minLon: 95.0, maxLon: 141.05, minLat: -11.05, maxLat: 6.05 };
const VB_W = 900;
const VB_H = Math.round((VB_W * (BOUNDS.maxLat - BOUNDS.minLat)) / (BOUNDS.maxLon - BOUNDS.minLon));
const TOL = 0.045; // simplification tolerance in degrees

function project([lon, lat]) {
  const x = ((lon - BOUNDS.minLon) / (BOUNDS.maxLon - BOUNDS.minLon)) * VB_W;
  const y = ((BOUNDS.maxLat - lat) / (BOUNDS.maxLat - BOUNDS.minLat)) * VB_H;
  return [Math.round(x * 10) / 10, Math.round(y * 10) / 10];
}

function perpDist(p, a, b) {
  const [x, y] = p, [x1, y1] = a, [x2, y2] = b;
  const dx = x2 - x1, dy = y2 - y1;
  const len2 = dx * dx + dy * dy;
  if (len2 === 0) return Math.hypot(x - x1, y - y1);
  let t = ((x - x1) * dx + (y - y1) * dy) / len2;
  t = Math.max(0, Math.min(1, t));
  return Math.hypot(x - (x1 + t * dx), y - (y1 + t * dy));
}

function simplify(points, tol) {
  if (points.length < 3) return points;
  let maxD = 0, idx = 0;
  for (let i = 1; i < points.length - 1; i++) {
    const dist = perpDist(points[i], points[0], points[points.length - 1]);
    if (dist > maxD) { maxD = dist; idx = i; }
  }
  if (maxD > tol) {
    const left = simplify(points.slice(0, idx + 1), tol);
    const right = simplify(points.slice(idx), tol);
    return left.slice(0, -1).concat(right);
  }
  return [points[0], points[points.length - 1]];
}

function fetchJson(url) {
  return new Promise((resolve, reject) => {
    https.get(url, (res) => {
      if (res.statusCode >= 300 && res.statusCode < 400 && res.headers.location) {
        return resolve(fetchJson(res.headers.location));
      }
      let body = "";
      res.on("data", (c) => (body += c));
      res.on("end", () => resolve(JSON.parse(body)));
    }).on("error", reject);
  });
}

async function main() {
  const geojson = await fetchJson(SOURCE_URL);
  const provinces = [];

  for (const f of geojson.features) {
    const name = f.properties.Propinsi;
    const geom = f.geometry;
    const polys = geom.type === "Polygon" ? [geom.coordinates] : geom.coordinates;
    let dStr = "";
    for (const poly of polys) {
      for (const ring of poly) {
        if (ring.length < 4) continue;
        const simplified = simplify(ring, TOL);
        if (simplified.length < 4) continue;
        const pts = simplified.map(project);
        dStr += `M${pts[0][0]},${pts[0][1]} ` + pts.slice(1).map((p) => `L${p[0]},${p[1]}`).join(" ") + " Z ";
      }
    }
    if (dStr) provinces.push({ name, d: dStr.trim() });
  }

  const out = `// Auto-generated by scripts/gen-indonesia-map.js from ans-4175/peta-indonesia-geojson.
// Do not hand-edit — rerun the script to regenerate.
export const INDONESIA_VIEWBOX = "0 0 ${VB_W} ${VB_H}";

export const INDONESIA_PROVINCES = ${JSON.stringify(provinces)};

export function projectLonLat(lon, lat) {
  const minLon = ${BOUNDS.minLon}, maxLon = ${BOUNDS.maxLon}, minLat = ${BOUNDS.minLat}, maxLat = ${BOUNDS.maxLat};
  const x = ((lon - minLon) / (maxLon - minLon)) * ${VB_W};
  const y = ((maxLat - lat) / (maxLat - minLat)) * ${VB_H};
  return [x, y];
}
`;

  fs.writeFileSync(OUT_FILE, out);
  console.log(`wrote ${OUT_FILE} (${provinces.length} provinces, ${(out.length / 1024).toFixed(1)} KB)`);
}

main();
